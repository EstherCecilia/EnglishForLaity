    public void initFX(JFXPanel fxPanel) { //cria o MediaPlayer responsavel pelo controle do video
        switch(aux){//o valor de aux determina qual endereco de video sera carregado
            case 0:
                VIDEO_URL = getClass().getResource("FRIENDS.mp4").toString();
               
                break;   
            case 1:
                VIDEO_URL = getClass().getResource("Ed Sheeran - Photograph.mp4").toString();
                
                break;
            case 2:
                VIDEO_URL = getClass().getResource("The Chainsmokers & Coldplay - Something Just Like This.mp4").toString();
               
                break;
            case 3:
                VIDEO_URL = getClass().getResource("music.mp4").toString();
                
                break;
            case 4:
                VIDEO_URL = getClass().getResource("Friends  - Marshmello & Anne-Marie.mp4").toString();
                
                break;
            case 5:
                VIDEO_URL = getClass().getResource("Rag'n'Bone Man - Human.mp4").toString();
                
                break;
            
        }
        
        Media media = new Media(VIDEO_URL);
        
        mediaPlayer = new MediaPlayer(media);
        MediaView mediaView = new MediaView(mediaPlayer);

        StackPane raiz = new StackPane();
        raiz.getChildren().add(mediaView);
        Scene cena = new Scene(raiz, 600, 400);
        mediaPlayer.play();
        
        fxPanel.setScene(cena);
    }
