    private void conferirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_conferirActionPerformed
        // TODO add your handling code here:
        String resp = traducao.getText();
        int aux = 0;
        System.out.println(resp + "\n");

        for (int i = 0; i < traducoesVet.length; i++) { //percorre as traducoes e confere se o usuario digitou alguma das traducoes
            if (resp.compareToIgnoreCase(traducoesVet[i]) == 0) {
                ImageIcon icon = new ImageIcon(getClass().getResource("v.png"));
                resposta.setIcon(icon);
                resposta.setText("Rsposta correta");
                resposta.setVisible(true);
                jogador.setPontos(jogador.getPontos() + 5);
                aux = 1;
                break;
            }
        }
        if (aux == 0) {
            ImageIcon icon = new ImageIcon(getClass().getResource("x.png"));
            resposta.setIcon(icon);
            resposta.setText("Resposta incorreta");
            resposta.setVisible(true);
            correcao.setText("A tradução correta é:" + linhaVet[3]);

        }

    }//GEN-LAST:event_conferirActionPerformed

