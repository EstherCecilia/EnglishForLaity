    public void lerArq() throws FileNotFoundException, IOException { //copia as palavras que o usuario ja viu para o array palavras
        FileReader fr;
        BufferedReader readerfr = null;
        String linha;
        int cont = 0;
        fr = new FileReader("palavras.txt");
        readerfr = new BufferedReader(fr);

        do {
            linha = readerfr.readLine();
            palavras.add(linha);
            cont++;

        } while (linha != null && cont < jogador.getOndeParei());

        for (int i = 0; i < palavras.size(); i++) {
            System.out.println(palavras.get(i) + "\n");
        }

    }

